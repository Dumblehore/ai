async function main() {
  console.log(`Adding ${newQuestions.length} questions from Drive question bank...`);

  let inserted = 0;
  let skipped = 0;

  for (const q of newQuestions) {
    try {
      await prisma.question.upsert({
        where: { id: q.id },
        update: q,
        create: q,
      });
      inserted++;
    } catch (e) {
      console.warn(`Skipped question ${q.id}:`, e.message);
      skipped++;
    }
  }

  console.log(`✓ Done! Inserted/Updated: ${inserted}, Skipped: ${skipped}`);
  
  const total = await prisma.question.count();
  console.log(`Total questions in database: ${total}`);
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
